//$                                   Ahora bien, vamos a explicar el ciclo for !!!!
/*
* Linea 14:
Se inicializa el contador ( declaramos contador y le asignamos el valor de 0)esta vendria a ser la expresión inicial; se establece la 
condición que vamos a evaluar hasta que nos de falso para terminar el programa; declaramos la expresión final, que es la que va ir 
(++) incrementando o (--) decrementando el valor de la expresión inicial. Se abre la sentencia a ejecutar cada ves que la condicion 
de como resultado verdadero.
* Linea 15:
Muestra en consola el valor del resultado de la condición ( contador )
* Linea 16:
muestra en consola( 'Fin ciclo for').
*/
for(let contador = 0; contador < 3 ; contador++ ){   
    console.log(contador);                           
}                                                    
console.log("Fin ciclo for");                        
